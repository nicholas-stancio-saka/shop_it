class Product {
  final int id;
  final String imageUrl;
  final String name;
  final String description;
  final String rating;
  final String reviewCount;
  final double price;

  Product({
    required this.id,
    required this.imageUrl,
    required this.name,
    required this.description,
    required this.rating,
    required this.reviewCount,
    required this.price,
  });
}
